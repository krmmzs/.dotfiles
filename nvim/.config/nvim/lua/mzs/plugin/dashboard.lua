local function get_file()
    local x = {}
    local idx = 0
    for file in lfs.dir(path) do
        if file ~= "." and file ~= ".." then
            x[idx] = file
            idx = idx + 1
        end
    end
    math.randomseed(os.time())
    local index = math.random(0, idx - 1)
    return x[index]
end

